# Temperatura en celsius

temp_celsius = 50

#Formula Fahrenheit
temp_Fahrenheit = (temp_celsius * 9 / 5) + 32

#Formula Kelvin
temp_kelvin = temp_celsius + 273.15

# Mostrar resultados
print(f"Temperatura en Celsius: {temp_celsius}°C")
print(f"Temperatura en Fahrenheit: {temp_Fahrenheit}°F")
print(f"Temperatura en Kelvin: {temp_kelvin}K")
